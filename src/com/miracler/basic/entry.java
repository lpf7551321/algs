package com.miracler.basic;

public class entry {
	private int key;
	private int value;

	public entry(int k, int v) {
		this.key = k;
		this.value = v;
	}

	public int getKey() {
		return key;
	}

	public void setKey(int key) {
		this.key = key;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
}
